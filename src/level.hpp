class tile 
{
    public:

    SDL_Texture* tiletext = NULL;

    int width=64;
    int height=64;

    int x; //x and y coords on the map
    int y;
    int screen_x = x-CAMX;
    int screen_y = y-CAMY;
     
    int sx; // x and y coords on the sprite sheet
    int sy;
    
    bool isobstacle=false;
    
     tile ()
    {
         x=0;
         y=0;
         sx=0;
         sy=0;
    }


    tile ( SDL_Texture* text)
    {
         tiletext = text;
         x=0;
         y=0;
         sx=0;
         sy=0;
    }

    tile ( SDL_Texture* text, int sxn , int syn)
    {
          sx=sxn;
          sy=syn;
          tiletext = text;
    }
     
    void rendertile ( SDL_Renderer* renderer , int level_index )
    {
         screen_x = x - CAMX;
         screen_y = y - CAMY;

         SDL_Rect src;
         src.x=sx;
         src.y=sy; 
         if( level_index == 2){
         src.w=16;
         src.h=16;            
         }
         else {
         src.w=32;
         src.h=32;
         }

         SDL_Rect dest;
         dest.x=screen_x;
         dest.y=screen_y;
         dest.w=64;
         dest.h=64;
        
          
          SDL_RenderCopy( renderer , tiletext , &src , &dest );

     }

     void copytile ( tile tileset_tile ) // for copying the tileset data onto the map data
     {
         
         tiletext = tileset_tile.tiletext;
         sx=tileset_tile.sx;
         sy=tileset_tile.sy;
     }

};


class level
{
     public:
     string name;
     
     vector <tile> tiles_layer1;
     vector <tile> tiles_layer2;
     vector <tile> tiles_layer3;
     vector <tile> tiles_layer4;
     
     int rows;
     int columns;

     int tile_width=32;
     int tile_height=32;

     vector <tile> tileset;

     int tileset_rows;
     int tileset_columns;

     TTF_Font* levelfont=NULL;
     SDL_Texture* font = NULL;
     bool fontstart = false;

     vector <powerup> powerups;
     int enemy_count = 0;
     
     Music level_bgm;

     level()
     {
        rows=0;
        columns=0;
     }

     level ( int r , int c )
     {
          rows=r;
          columns=c;
     }

     level ( int r , int c , int tr , int tc )
     {
          rows=r;
          columns=c;
          tileset_rows = tr;
          tileset_columns = tc;
     }

     void destory_level()
     {
         tileset.clear();
         tiles_layer1.clear();
         tiles_layer2.clear();
         tiles_layer3.clear();
     }

     void set_level_dimensions( int r , int c , int tr , int tc)
     {
          rows=r;
          columns=c;
          tileset_rows = tr;
          tileset_columns = tc;
     }

     void set_tileset( string path , SDL_Renderer* ren )
     { 
          SDL_Surface* tile_surface = IMG_Load( path.c_str() );
          SDL_Texture* tileset_texture = SDL_CreateTextureFromSurface( ren ,  tile_surface );
      
          for( int i=0 ; i<tileset_rows ; ++i){
              for( int j=0 ; j<tileset_columns ; ++j){
                      
                     tile temp_tile( tileset_texture , j*tile_width , i*tile_height);
                     tileset.push_back( temp_tile );
              }     
          }

          SDL_FreeSurface( tile_surface );
     }
     
     
    void draw_layer( SDL_Renderer* ren  , vector <tile> layer  , int level_index)
    {
    for ( auto& tile : layer ) 
    {
        tile.rendertile(ren , level_index);
    }
    }
    
    void draw_level( SDL_Renderer* ren , int level_index)
    {
        draw_layer( ren , tiles_layer1 ,level_index);
        draw_layer( ren , tiles_layer2 ,level_index);
        draw_layer( ren , tiles_layer3 ,level_index);
    }
    
    void draw_tileset( SDL_Renderer* renderer ){
      
                               tile &temp = tileset[0];              
                               SDL_RenderCopy( renderer , temp.tiletext  , NULL , NULL);        
      
       
    }
};


